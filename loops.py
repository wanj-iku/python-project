# while loop
# incrementing a value
number = 5
while number <= 10:
    print(number)
    number += 1

# decrementing a value
num = 105
while num >= 100:
    print(num)
    num -= 1

# break statement
x = 1
while x <= 5:
    print(x)
    if x == 3:
        break
    x += 1

# Continue
count = 19
while count < 25 :
    count += 1
    if count == 23:
          continue
    print(count)
