# arithmetic operators - perform simple calculations
X = 60
Y = 7

print(X + Y)  # addition
print(X - Y)  # subtraction
print(X * Y)  # multiplication
print(X / Y)  # division
print(X % Y)  # modulus - returns the remainder

# comparison operators- compare values
A = 34
B = 23
print(A < B)
print(A > B)
print(A <= B)
print(A >= B)
print(A == B)  # equal to
print(A != B)  # not equal to

# Assigment operators
num = 56
print(num)

z = 2
z += 4
print(z)

number = 45
number *= 2
print(number)

# logical operators
num1 = 23
num2 = 16
print(num1 > num2 and num1 == num2)
print(num1 > num2 or num1 == num2)
print(not(num1 > num2 and num1 == num2))
